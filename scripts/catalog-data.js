  function hashString(value) {
    let hash = 2166136261;
    for (let index = 0; index < value.length; index += 1) {
      hash ^= value.charCodeAt(index);
      hash = Math.imul(hash, 16777619);
    }
    return hash >>> 0;
  }

  function normalizeText(value) {
    return String(value || '').toLowerCase().replace(/ё/g, 'е');
  }

  function escapeHtml(value) {
    return String(value).replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;').replace(/'/g, '&#39;');
  }

  function clampText(value, max) {
    return value.length > max ? `${value.slice(0, max - 1)}…` : value;
  }

  function shuffle(list) {
    const copy = [...list];
    for (let index = copy.length - 1; index > 0; index -= 1) {
      const swapIndex = Math.floor(Math.random() * (index + 1));
      [copy[index], copy[swapIndex]] = [copy[swapIndex], copy[index]];
    }
    return copy;
  }

  function pick(list, seed, offset) {
    return list[(seed + (offset || 0)) % list.length];
  }

  function buildAuthor(id) {
    const first = AUTHOR_PARTS.first[(id * 7 + 3) % AUTHOR_PARTS.first.length];
    const last = AUTHOR_PARTS.last[(id * 11 + 5) % AUTHOR_PARTS.last.length];
    const nickLeft = AUTHOR_PARTS.nickLeft[(id * 13 + 1) % AUTHOR_PARTS.nickLeft.length];
    const nickRight = AUTHOR_PARTS.nickRight[(id * 17 + 2) % AUTHOR_PARTS.nickRight.length];
    const patterns = [
      `${first} ${last}`,
      `${nickLeft}.${nickRight}`,
      `${first} ${nickLeft}`,
      `${nickLeft}_${nickRight}`,
      `${first} / ${nickRight}`,
      `${nickLeft}${(id % 83) + 11}`,
      `${first} ${last}-${(id % 9) + 1}`,
      `${nickLeft}.${nickRight}.ai`
    ];
    return patterns[id % patterns.length];
  }

