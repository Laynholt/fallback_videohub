  function splitPreviewText(value, maxChars, maxLines = 2) {
    const words = String(value).split(/\s+/).filter(Boolean);
    const lines = [];
    let current = '';

    for (const word of words) {
      const next = current ? `${current} ${word}` : word;
      if (next.length <= maxChars) {
        current = next;
        continue;
      }

      if (current) lines.push(current);
      current = word;

      if (lines.length === maxLines - 1) break;
    }

    if (lines.length < maxLines && current) lines.push(current);
    if (!lines.length) lines.push(clampText(value, maxChars));

    const visible = lines.slice(0, maxLines);
    if (words.join(' ').length > visible.join(' ').length) {
      visible[visible.length - 1] = clampText(visible[visible.length - 1], maxChars);
    }
    return visible;
  }

  function renderSvgLines(lines, x, y, options = {}) {
    const {
      fill = '#f8fafc',
      size = 18,
      weight = 700,
      lineHeight = 20,
      opacity = 1
    } = options;

    return `<text x="${x}" y="${y}" fill="${fill}" fill-opacity="${opacity}" font-family="Arial, sans-serif" font-size="${size}" font-weight="${weight}">${lines.map((line, index) => `<tspan x="${x}" dy="${index === 0 ? 0 : lineHeight}">${escapeHtml(line)}</tspan>`).join('')}</text>`;
  }

  function buildViews(id, title, dateLabel) {
    const hash = hashString(`${title}-${id}-${dateLabel}`);
    const ranges = {
      '2 часа назад': [10, 900],
      '5 часов назад': [20, 1400],
      'вчера': [60, 3200],
      '2 дня назад': [80, 5200],
      '3 дня назад': [100, 7000],
      'неделю назад': [120, 11000],
      '2 недели назад': [150, 13000],
      'месяц назад': [180, 15000],
      '2 месяца назад': [90, 15000]
    };
    const [min, max] = ranges[dateLabel] || [50, 8000];
    return new Intl.NumberFormat('ru-RU').format(min + (hash % (max - min + 1)));
  }

